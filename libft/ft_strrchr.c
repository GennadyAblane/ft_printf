#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		b;
	char	*z;

	b = ft_strlen(s);
	z = (char*)s;
	while (b >= 0)
	{
		if (z[b] == (char)c)
			return (&z[b]);
		b--;
	}
	return (0);
}
