int	ft_word(char const *str, char c)
{
	int	nw;
	int	i;

	nw = 0;
	i = 0;
	while (str[i] != '\0')
	{
		while (str[i] == c)
			i++;
		while (str[i] != '\0' && str[i] != c)
			i++;
		nw++;
		if (str[i - 1] && str[i - 1] == c && str[i] == '\0')
			nw--;
	}
	return (nw);
}
