int	ft_intlen(int n)
{
	int		end;
	int		mi;

	mi = 0;
	if (n < 0)
		mi = 1;
	end = 0;
	while (n != 0)
	{
		n = n / 10;
		end++;
	}
	return (end + mi);
}
