#include "libft.h"

char	*ft_strncpy(char *dest, const char *src, size_t n)
{
	size_t s;

	s = 0;
	while (s < n && src[s] != '\0')
	{
		dest[s] = src[s];
		s++;
	}
	while (s < n)
	{
		dest[s] = '\0';
		s++;
	}
	return (dest);
}
