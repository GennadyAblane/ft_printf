#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	size_t	p;

	p = 0;
	i = ft_strlen(s1);
	while (p < n && s2[p])
		s1[i++] = s2[p++];
	s1[i] = '\0';
	return (s1);
}
