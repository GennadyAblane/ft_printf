#include "libft.h"

void	ft_putendl(char const *s)
{
	int		n;
	char	c;

	c = '\n';
	n = 0;
	if (!s)
		return ;
	while (s[n] != '\0')
		ft_putchar(s[n++]);
	write(1, &c, 1);
}
