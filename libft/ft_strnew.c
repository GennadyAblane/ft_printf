#include "libft.h"

char	*ft_strnew(size_t size)
{
	void *new;

	if (size == (size_t)(-1))
		return (NULL);
	new = (void *)malloc(sizeof(void) * (size + 1));
	if (!new)
		return (NULL);
	return (char *)(ft_memset(new, '\0', size + 1));
}
