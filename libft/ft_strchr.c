#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*z;

	z = (char*)s;
	while (*z)
	{
		if (*z == (char)c)
			return (z);
		z++;
	}
	if (*z == (char)c)
		return (z);
	return (NULL);
}
